package com.company;

public class OperatorWork {
    public static void main(String[] args) {

        int a = 10;
        int b = 20;
        int c = a + b;
        System.out.println(c);
        if (c > 30) {
            System.out.println("Greater");
        } else {
            System.out.println("Lesser");
        }
    }
}
